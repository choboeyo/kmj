        <h2 class="page-title"><i class="far fa-question-circle" style="display:inline"></i> 구글 애널리틱스 코드 등록 방법</h2>
    </div>
    <div class="admin-help-wrap">
        <ul>
            <li>
                <p class="MT20 MB20">1. 구글 애널리틱스 설치를 위해 구글 애널리틱스 홈페이지로 이동합니다. <a href="https://www.google.com/analytics/" target="_blank" class="point-color">구글 애널리틱스로 이동하기</a><br>
                    페이지로 이동 후 로그인 또는 계정만들기를 클릭하여 새로운계정을 만들거나 로그인 합니다.<br>
                    <br>
                    <span class="point-color">가입</span> 버튼을 눌러 다음 페이지로 이동합니다.</p>
                <img src="/assets/images/admin/help/google_analytics01.png" alt="">
            </li>
            <li>
                <p class="MT20 MB20">2. 아래와 같은 페이지로 이동 후 정보를 입력합니다.</p>
                <ul style="padding-left:20px">
                    <li style="list-style:disc"> <span class="point-color">계정이름 설정</span> - 대표할수있는 이름을 입력합니다.</li>
                    <li style="list-style:disc"> <span class="point-color">웹사이트 이름 및 URL</span> - 웹사이트 주소를 입력합니다.</li>
                    <li style="list-style:disc"> <span class="point-color">업종카테고리</span> - 웹사이트의 주제에 맞게 선택합니다.</li>
                    <li style="list-style:disc"> <span class="point-color">보고서 시간대</span>- 시간대를 맞게 선택합니다. ex)대한민국</li>
                    <li style="list-style:disc"> 권장항목에 체크가 다 되어있다면 <span class="point-color">추적 ID 가져오기</span>버튼을 클릭합니다.</li>
                </ul>
                <br>
                <img src="/assets/images/admin/help/google_analytics02.png" alt="">
            </li>
            <li>
                <p class="MT20 MB20">3. 구글 애널리틱스 서비스 약관계약에 동의함을 클릭 하고,<span class="point-color">[웹사이트 추적]</span> 코드를 복사합니다.<br></p>
                <img src="/assets/images/admin/help/google_analytics03.png" alt="">
            </li>
            <li>
                <p class="MT20 MB20">4. 복사한  <span class="point-color">[웹사이트 추적]</span>코드를 휘파람 보드 관리자  -> <span class="point-color">구글 애널리틱스 코드</span> 부분에 붙여넣고 저장 합니다.</p>
                <img src="/assets/images/admin/help/google_analytics05.png" alt="">
            </li>
            <li>
                <p class="MT20 MB20">5. 구글 애널리틱스 설치가 완료되었습니다.</p>
            </li>
        </ul>
